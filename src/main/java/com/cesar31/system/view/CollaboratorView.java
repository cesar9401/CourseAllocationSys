    private void searchUButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchUButtonActionPerformed
        // TODO add your handling code here:
        String id = idUTxt.getText();
        if (!id.trim().isEmpty()) {
            control.getEdd().getUsers().getNode(id);
            if (control.getEdd().getUsers().getNode(id) != null) {
                User u = control.getEdd().getUsers().getNode(id).getData();
                if (!u.getType().equals(UserType.SUPER)) {
                    userTxt.setText(u.getUsername());
                    passUText.setText(u.getPassword());
                    // int index = u.getType().equals(UserType.STUDENT) ? 0 : 1;
                    typeUCombo.setSelectedIndex(u.getType().equals(UserType.STUDENT) ? 0 : 1);
                } else {
                    showMessage("El usuario " + id + ", no existe.", "Informacion");
                }
            } else {
                showMessage("El usuario " + id + ", no existe.", "Informacion");
            }
        }
    }//GEN-LAST:event_searchUButtonActionPerformed

